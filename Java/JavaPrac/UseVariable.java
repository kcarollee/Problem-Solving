class UseVariable
{
    public static void main (String[] args)
    {
        int num1;
        num1 = 10;

        int num2 = 20;
        int num3 = num1 + num2;

        double a = 2.3;
        double b = 2.4;
        //bool in java is boolean.
        System.out.println(num1 + " + " + num2 +  " = " + num3);
        System.out.println(a + b);
    }
}